package config;

/**
 * Перечисление типов конфигурационных значений.
 * Используется для явного указания типа при чтении конфигурации.
 */
public enum ConfigType {

    /** Автоматическое определение типа на основе типа поля */
    AUTO,

    STRING,
    INTEGER,
    BOOLEAN,
    LONG,
    DOUBLE
}
